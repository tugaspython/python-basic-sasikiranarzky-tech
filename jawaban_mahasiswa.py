def tambah(a, b):
    """
    Fungsi untuk menjumlahkan a dan b.
    """
    return a + b
 
def kurang(a, b):
    """
    Fungsi untuk mengurangkan a dengan b.
    """
    return a - b 
  
def kali(a, b):
    """
    Fungsi untuk mengalikan a dan b.
    """
    return a * b 
   

def bagi(a, b):
    """
    Fungsi untuk membagi a dengan b.
    """
    if b == 0 : 
        return "Error: Pembagian dengan nol tidak diperbolehkan"
    return a / b 
   



